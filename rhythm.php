      <h2>The concept of rhythm in Indian music</h2>
      <p> In India, music has been associated with the period of time. It is believed that the gods were the first to give birth to music and musical instruments with many deities are also added; Damro to Shiva, Pakhawaj to Ganesh, Veena to mother Saraswati, Flute to Shri Krishna, etc. In India, music is seen in metaphysics, perhaps because of which honor is given to music and musical instruments in India, it can not be found anywhere else in the whole world. In ancient musical texts, music is given only for singing, playing and Dancing . The smallest unit of measuring time in the Indian music period is Matra or Beat and the rhythm is formed from the group of Matras or Beats. The rhythm is the cornerstone of music in which it attains its original purpose. It has been said in the context of Taal that 'T' was created by Tandava Dance of Lord Shiva's and 'L' was created from the Lasya Dance of Parvati.</p>
      <p>According to Ancient Musical text Sageet Makrand, the continent, which is a recurring period of time, which holds the fountain of the song, instrumental and dance on itself, is called the rhythm.</p>
      <p>Based on the rhythm, it is a process that provides a proper and meaningful form to any musical Composition. Just as the threaded pearl in a garland looks beautiful after clinging together in one's lanyard, the music (singing, playing, Dancing) tied in the rhythm looks also meaningful and beautiful.</p>
      <p>Indian musical texts have recorded 10 lives of rhythm – Time or Duration, Path, Action, Grouping, Position, Type or Classification, Number of Sound Syllables in a Matra or Beat, Time Progression, Yati(Change in Speed) and Prastar(The way you improvise a rhythm) .</p>
