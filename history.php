        <p class="para ">There was a smaller instrument known as dukkar (about two times the size of the tabla) that may have been the next step down from the much larger nagara. In the fifteen and sixteen hundreds, the dukkar needed to downsize even further because in the presence of the king, one could not sit, thus the musicians were required to stand and play a smaller, lighter instrument. There are paintings over 300 years old depicting tabla players with a platform tied to their waist, upon which the tabla sits. Also, the invention of the perminent Gob or Shyhi also is only 300 years old, thus all drums prior to this time used a dough which was applied and removed every time the drum was used. Tabla rose to greater popularity in the royal courts of Delhi during the late 17th and 18th centuries and today is considered the premier percussion instrument of India.</p>
        <p class="para ">The individual usually credited with the most significant developments of tabla technique and repertoire at this time was Sidhar Khan Daadhi Pakhwaji. His grandsons and their various disciples carried the art of tabla playing to other major centers of North Indian cultural life, a dispersion which naturally led to the evolution of a number of distinct regional performance styles. At the present time these schools of tabla playing or gharanas (derived from the Hindi word ghar or 'house') commonly include those of Delhi, Lucknow, Benares, Farukhabad and Ajrara. A sixth style, the 'Punjab gharana', developed independently in what is present-day Pakistan as a pakawaj gharana until the late 19th century when those compositions were applied to the tabla. (Ustad Alla Rakha & Ustad Zakir Hussain). Each gharana has imbibed the essence, culture and character of its founding city/area into its tradition. However in modern times, the event of travel and technology have allowed the students of all gharanas to share techniques and compositions to the point of blending styles. There of course are up sides and downsides to this modern day evolution, but regardless of all else one thing remains true, the tabla is a dynamic and rapidly evolving instrument.</p>
