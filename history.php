        <p class="para ">The grandfather of the tabla is a drum called pakhawaj. This is a two-sided barrel drum with both a treble end and larger bass end. A handmade dough mixture gob is applied each time it is played and then removed when finished. The two sided barrel drum, known as mridang, can be traced back more than 2000 years with the pakhawaj appearing perhaps 1000 years ago. Its quality of sound and its playing style evolved in tandem with the vocal music of the time and is famed for its rich, full tones and over the centuries become recognized not only as an accompanying instrument, but as a solo instrument. The evolution from pakhawaj to tabla was not the mythic story of someone breaking a pakhawaj in two. The historical understanding is that there was an instrument called nagara/naqara, which comes from the family of war drums, like kettledrums, which were hung on either side of an elephant, thus constituting a pair of drums.</p>
