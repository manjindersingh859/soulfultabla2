    <h2>Lucknow Gharana</h2>
    <div class="row">
      <div class="col-md-8">
        <p class="para"> Lucknow gharana (also known as the Poorab Gharana) is said to be the second gharana to appear in India around one hundred years following the founding of the Delhi gharana. It branched out of Delhi when two brothers Modu and Baksu Khan moved to Lukhnow during the 18th century. They collaborated with Kathak dancers of Lukhnow to create a unique style of playing which reflected the foot work of the dancers. The tabla soon became the drum of choice for dance accompaniment because of its subtle and intricate tone. Compositions such as gat and paran became very popular in the Lukhnow gharana because of their beauty both spoken and played both by the dancer and tabla player.</p>
        <p class="para">This gharana is also a branch of Delhi Gharana. On the invitation of Nawab of Lucknow <strong>U.Modu Khan and U.Bakshu Khan </strong>of Delhi Gharana was sent to Lucknow, they created a new style named ‘<strong>Lucknow Gharana</strong>’ with their efforts and hard work. The influence of the musical and social environment of this region became the cause of innovation in their style, so that they laid the foundation of a new Gharana.<strong> U. Mohammad Khan, U. Abid Hussain, U. Wajid Hussain, U. Afaq Hussain, Pt. Hirendra Ganguly,Ilmas Khan, Hiru Ganguly,Santosh Kishan Biswas,Sheikh Dawood Khan,Swapan Chaudhuri,Shabbir Nisar,Anil Bhattacharjee</strong>, etc. .. are in the elite cast of this Gharana.</p>
