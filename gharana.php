    <h2>Farukhabad Gharana</h2>
    <div class="row">
      <div class="col-md-8">
        <p class="para"> Farukhabad gharana derives its name from Farukhabad in Uttar Pradesh state. It was created by Ustad Haji Vilayat Ali Khan, disciple of Ustad Miyan Bakshu Khan of Lucknow, and it is for his birthplace the gharana is named. Being a prominent composer and performer, he was appointed as court musician in Rampur and passed his tradition on through his three sons, Nisar Ali Khan, Aman Ali Khan, and Hussain Ali Khan, and their disciples. (1700 - 1750 which was around the time Farukhabad gharana was founded)</p>
        <p class="para">This gharana came into existence when U. Bakshu Khan married his daughter to Haji Vilayat Ali of Farukhabad and gave him higher education of tabla. After that he created many disciples and recognized a new gharana.<strong> U. Imam Baksh, U. Hussein Ali Khan, U.Salari Khan, U. Munir Khan, U. Ahmed Jan Thirakwa, U. Amir Hussain, U. Karamat Ullah, U. Maseet Khan, U. Sabir Khan</strong>, etc. These are the most prominent artists of the Gharana. Because this family was the branch of Lucknow, there is also a sense of style in the Gharana. Apart from this, gat, rau and chhands are also used in this Gharana.</p>
