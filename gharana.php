        <p class="para">It is important to note that the tradition of peshkar did not exist in Punjab tabla at all until Ustad Alla Rakha appeared. There were some kaida and rela compositions present from the other tabla gharanas, but the main meat of the Punjab Gharana was the set compositions such as Gat, Paran and Chakradar. As a matter of fact, when Ustad Alla Rakha first began presenting the concept of peshkar (D..rD.Di.N.D.t.D.D.Di.N.D..rD.Di.N. etc...) the conesiours did not understand what he was developing and it took time for this new category of composition to be accepted. This new way of presenting the sounds of the tabla were akin to the alop, jor, jhala in raga development. It was also Ustad Alla Rakha who began introducing much more intricate "upaj" (improvization) within kaida and rela. Subtle variations focusing on stretching unusual, odd metered phrases (Sankirna Jati) across the beats became a signature of Ustad Alla Rakha and thus the Punjab Gharana. And today the legendary Ustad Zakir Hussain has taken the presentation and exploration of peshkar and lay curry (mixing meters) to an unmatched height.</p>

