    <h2>Ajrada Gharana</h2>
    <div class="row">
      <div class="col-md-8">
        <p class="para"> Ajrada gharana is also one of the initial off shoots of the Delhi gharana and grew in strength following the migration of tabla to Lucknow (1700 - 1750 which was around the time Farukhabad gharana was founded). Actually, a senior student of the Delhi gharana, Kallu Khan was the one who brought tabla to that area which is why the Ajrada gharana is known as the "shagira" (disciple school) of Delhi. Ajrada is named after a small village in the Meerut district of Utter Pradesh very near Delhi.</p>
        <p class="para">There is a Village named Ajrada in Meerut. There were Kallu Khan and Meeru Khan, who were brothers. They learnt Tabla from Ustad Sitaab Khan and went back to their home town. Where they thought many disciples and with their untiring efforts, they laid the foundation of a new gharana called ‘’Ajrada Gharana’’. </p>
        <p class="para"> <b> U. Shammu Khan, U. Chand khan, U. Kale Khan, U. Habibuddin Khan, U. Manju Khan, U. Hashmat Khan, U. Akram Khan,</b>etc. ... are the prominent artists of this Gharana.</p>
        <p class="para"> The characteristic of this gharana is the use of Adi Laya, the phrases of this gharana are meant to be played in adi or devadi laya and faster than Delhi Gharana compositions.</p>
