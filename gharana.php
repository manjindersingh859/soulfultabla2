  <h1>Gharanas of Tabla</h1>
  <div class="container" style="margin-bottom: 50px;">
    <p class="para">The word gharana comes from the Hindi word 'ghar', which means 'family' or 'house'.The names given to particular gharanas are often derived from the region or city in which they were developed.</p>

    <p class="para">Tabla gharanas came into existence perhaps 300 years after the tabla itself appeared in India. Evidence of the tabla accompanying the vocal style called "Khayal" began to show up during the time of Amir Khusro 1196 - 1316 AD. The tradition of tabla solo, thus gharana, emerged in Delhi around the 15th century and was a nobel tradition taken into the Mughal courts of the time. One possible version of how the different gharanas began, is that the three sons of tabla master Sudhar Khan Dhadhai from Delhi were said to have traveled to the other regions of India where they were commissioned as court performers. Thus, as those maestros settled into those areas and took on students and families, each regions special character and unique flavor gradually influenced the style.</p>

    <h3> Basic Style Distinctions:</h3>
    <p class="para"> There are two main styles of playing which certain compositions gravitate towards and some of the gharanas favor. One is known as Bhand B'nd Baaz and the other is khulaa Baaz. Baaz comes from the Urdu word "Bajanaa" (to play an instrument). The word Bhand means "bound" or "to bind" and in tabla, refers to a closed or muffled sound on the drum. The word khulaa means "open" or "resonant" and refers to the long, ringing tones produced on the tabla. Thus, the Khulaa Baaz refers to a style of playing which allows the tones of the drum to sustain and ring out. The pakawaj (ancestor of the tabla) was generally played using this approach. As the compositions and the solo tradition of the tabla evolved, the speed of the Kaidas, Relas and specialty compositions didn't allow for the hand to leave the drum as well as the need to control the open tones, so each stroke could be heard cleanly. Thus a tighter, more compact and much faster Bhand Baaz evolved.</p>
    <h3>Evolution:</h3>
    <p class="para">Each gharana came to be because of a great guiding light. An artist who broke into a new, unique expression of the art form and inspired students and family to gather and carry that genius forward. And in the early day of the tabla about 400 years, there was an absence of mass transportation and communication, thus each region of India was allowed to develop it's own special approach, style and interpretation of the tabla.</p>

    <p class="para">In modern times many of these gharana distinctions have, to varying degrees, faded as the information has come to be more freely shared (Domestic & International touring, books, CDs and the internet). The newer generation of tabla players have learned parts of the tradition of various gharanas which has its own value and beauty, but some scholars are concerned at the prospect that the era of the distinct gharana has largly come to an end.</p>
    <h1 id="subhead">SIX MAIN GHARANAS OF TABLA:</h1>
    <h2>Punjab Gharana</h2>
    <div class="row">
      <div class="col-md-8">
        <p class="para"> Punjab Gharana, is a style and technique of Tabla playing that originated in the Punjab region of what is now split in present day Pakistan and India. The Punjab gharana was originally a pakhawaj gharana which existed long before the tabla arrived in that region. </p>

        <p class="para">Lala Bhavanidas was responsible for holding the foundation of the Punjab Gharana and was considered the premier pakawaj player of his day. During the period just before the creation of the Delhi Gharana, Lala Bhavanidas would travel to Delhi to participate in the pakawaj competitions which were held there each year. It was said that Sudhar Khan Daadhi, Delhi's premier pakawaj player, began playing the tabla after loosing the competition to Lala Bhavanidas a number of times. Thus Sudhar Khan Daadhi began developing a new and unique style specifically sculpted for the tabla from which the original tabla gharana sprang.</p>

        <p class="para"> Around the turn of the 20th century, Fakir Baksh, disciple of Saddu Hussain Baksh was the foremost advocate and he was the one responsible for bringing the pakawaj compositions onto the tabla. This marked the beginning of the Punjab Gharana appearing on the scene as a unique tabla gharana as apposed to it's main reputation as being the hotbed of pakawaj. His followers were Qadir Baksh II and Firoz Khan of Lahore. Jnan Prakash Ghosh of Farukhabad gharana was also a follower of Firoz Khan. Kader Baksh II was the teacher of the most renowned tabla player of 20th century,<strong> Ustad Alla Rakha Father and teacher of Ustad Zakir Hussain.</strong></p>
      </div>

