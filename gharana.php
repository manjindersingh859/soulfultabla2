    <p class="para">यह घराना तबले के बाकी सभी घरानों से अलग है, इसकी वादन-शैली से प्रतीत होता है कि यह स्वतंत्र रूप से विकसित हुआ है | इस घराने की नीव<strong>उ. फ़कीर बक्श </strong> ने रखी |<strong>उ. करमइलाही, उ. मल्लन खां, उ. कदीर बक्श, उ.अल्लाहरक्खा </strong> तथा उनके पुत्र<strong> उ. जाकिर हुसैन</strong> इस घराने के विश्व विख्यात कलाकार है |</p>
    <p class="para">यहां की वादन-शैली पखावज के बोलों को बंद करके बजाने की होने के कारण बाकी सभी घरानों से अलग है | यहाँ लयकारी का काम विशेष रूप से देखने को मिलता है|</p>
    <p class="para">उपरोक्त सभी घरानों को दो भागों में बांटा जा सकता है- पूरब तथा पश्चिम | पूरब बाज के अंतर्गत- लखनऊ घराना, बनारस घराना तथा फरुखाबाद घराना आता है और पश्चिम बाज के अंतर्गत दिल्ली घराना और अजराडा घराना आते है | पंजाब घराना स्वयं विकसित होने के कारण इन श्रेणीयों से अलग है |</p>
