    <h2>Banaras Gharana</h2>
    <div class="row">
      <div class="col-md-8">
        <p class="para">The Benares tabla gharana was developed about 200 years ago. The founder was the legendary Pandit Ram Sahai (1780–1826). Ram Sahai began studying the tabla with his father from the age of five. At the age of nine, he moved to Lucknow to become the disciple of Modhu Khan of the Lucknow gharānā.</p>
        <p class="para">The gharana is categorized into the Purbi (eastern) baj, which includes the Farukhabad, Lucknow, and Benares gharanas. This powerful playing style makes use of the more resonant strokes of tabla, such as Na, and Din. Benares players preferentially use the full-hand TeTe strokes, rather than the single finger alternation preferred by the Delhi style. The Benares baj makes use of over twenty different compositional types. There is extensive use of full hand "din" strokes which are very prevalent on pakhawaj.</p>
        <p class="para">Pandit Ram Sahai, the first tabla-player of this gharana, who got his education from <strong>U. Modu Khan</strong> of Lucknow and laid the foundation of the Banaras Gharana. Then his son Pt Bhairav ​​Sahai and grandson Pt Baldev Sahai and Great Grand Son Pt. Durga Sahai carried forward this lineage.<strong> Pandit Kanthe Maharaj, Pt. Kishan Maharaj, Pt Anokhe Lal, Pt. Bikku Ji, Pt Samta Prasad ji</strong>, etc. are the prominent artists of this Gharana.
          This gharana is more famous for its accompaniment. In this style Laggi, Ladi, parn, rela, chakradar and chhand are most famous and used. This Gharana is the highest in terms of Thumri, Dance and Solo-Playing.</p>
