    <h2>Delhi Gharana</h2>
    <div class="row">
      <div class="col-md-8">
        <p class="para"> The Delhi Gharana is considered the oldest of the tabla gharanas. As the strength of the tabla tradition grew in Delhi around the time of Emperor Akbar, It replaced its ancestor, the pakawaj because of its softer and more subtle tone.The Delhi tabla style is famous for its vast and rich repertoire of Kaidas. Delhi compositions are very melodic with concise development of kaidas, peshkars, and other theme/variation compositions.</p>
        <p class="para">It is also particularly famous for focusing on the style of playing know as "two finger" tabla. Meaning that
          much of the playing is done with only the pointer and middle finger strokes. This adds a clarity and focus to the sound which gives it distinction from the thicker, louder styles of the other gharanas. It poured out into other areas of India settling in the courts and in the other musical regions thus establishing a strong foundation of Delhi tabla repertoire, which over time evolved to blend with the customs and musical flavors of the areas in which it settled.</p>
