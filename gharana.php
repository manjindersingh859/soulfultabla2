    <h2>तबले के घराने</h2>
    <p class="para"> इस विषय में चर्चा आरम्भ करने से पहले ये जानना अति आवश्यक है की “घराना” क्या होता है | इसे हम वंश परम्परा भी कह सकते है | जिस प्रकार वंश आगे बढ़कर घराने का रूप धारण कर लेता है ठीक उसी प्रकार जब एक वादन-शैली, गुरु-शिष्य परम्परा से होती हुई विस्तार करती है तो <strong>घराने</strong> की शक्ल इख़्तियार कर लेती है | भारतीय संगीत में तबले के कुल छ: घरानों को मान्यता प्राप्त हुई- दिल्ली घराना, अजराडा घराना, लखनऊ घराना, फरुखाबाद घराना, बनारस घराना और पंजाब घराना | ये सभी घराने एक दुसरे से अपनी वादन क्रिया, सबक तथा निकास के आधार पर एक दुसरे से अलग हैं | आइये अब इन घरानों के निर्माण, विकास तथा विशेषताओं पर चर्चा करें |</p>
