		<p class="para">The Thirty Five Taal System is the foundation and most important part of <strong> South Indian Classical Music</strong>.There are seven main Taal in south indian music -<strong>Ek-Rupak,Jhamp, Triput, dhruv, mathh, atth</strong>.The matra value of laghu in a taal is 4 by default, but when we change the jati of the taal,we change the value of laghu. Which directly effects the number of matras and forms a new taal. This is the method how 7 main taals transform into 35 taals. Laghu has five Jaatis
			<strong>Tisra(3),Chaturasra(4),Khanda(5),Misra(7)and Sankeerna(9)</strong>; and by incorporating 7 Talas X 5 Jaatis, It creates a total of 35Different Talas.For Example, Dhruva Tala which is representing chatushrajati has 4 2 4 4 ( | O | | )have one Laghu of Chaturasra Jaati consist of 4 beat power,one Drut of 2 beats followed by two Laghus of 4 beats each.So,It forms a taal with total number of 14 Beats Now,When we change the power of laghu to 3 to transform it’s jati to Tisra Jaati.It becomes 3 2 3 3 ( | O | | ) 11 Beats Taal. This method is used to change jati of the taal which creates 35 Taals.Now we are adding a chart of all 7 taals showing change of jati for your reference.</p>
